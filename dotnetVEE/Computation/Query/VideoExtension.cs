namespace dotnetVEE.Computation.Query
{
    /// <summary>
    /// Represents an extension (video format) of a video.
    /// </summary>
    public enum VideoExtension : byte
    {
        /// <summary>
        /// The extension is MP4.
        /// </summary>
        Mp4,
        
        /// <summary>
        /// The extension is AVI.
        /// </summary>
        Avi,

        /// <summary>
        /// The extension is WMV.
        /// </summary>
        Wmv,

        /// <summary>
        /// The extension is MOV.
        /// </summary>
        Mov,

        /// <summary>
        /// The extension is MKV.
        /// </summary>
        Mkv,

        /// <summary>
        /// The extension is FLV.
        /// </summary>
        Flv,

        /// <summary>
        /// The extension is WEBM.
        /// </summary>
        Webm
    }
}
